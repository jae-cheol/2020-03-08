package contents.interfacee.ex1;

public class SamsungTV {
	public SamsungTV() { System.out.println("삼성TV를 구매하였습니다."); }
	public void turnOn() { System.out.println("TV전원 ON"); }
	public void turnOff() { System.out.println("TV전원 OFF"); }
	public void channelUp() { System.out.println("TV채널 UP"); }
	public void channelDown() { System.out.println("TV채널 DOWN"); }
	public void soundUp() { System.out.println("TV음량 UP"); }
	public void soundDown() { System.out.println("TV음량 DOWN"); }
}